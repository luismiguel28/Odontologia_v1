            <h2>Factores de riesgo en la Aparicion de caries dental</h2>
            <p>Los factores de riesgo que influyen en la formación de caries dental son bien conocidos y en la mayoría de los casos evitables, lo que permite desarrollar una estrategia preventiva basada en la educación sanitaria de la población. Pese a ello, la caries es un problema dental muy frecuente.</p>
            <ul>
                <li><b>Higiene oral deficiente:</b> si no se lavan los dientes o se hace de forma inadecuada,se favorece el depósito y acumulación de placa bacteriana sobre el esmalte de l as piezas dentales. </li>
                <li><b>Dieta rica en azúcares y carbohidratos:</b> el azúcar y el abuso de hidratos de carbono, especialmente si se ingiere fuera de las comidas, favorece la actividad de las denominadas bacterias caraiogénicas</li>
                <li><b>Sequedad bucal:</b> los factores que promueven la pérdida de flujo salival o sequedad de boca (tabaco, alcohol, trastornos de las glándulas tiroideas y otras enfermedades y tratamientos médicos) aumentan la actividad bacteriana que acaba en la formación de caries.</li>
                <li><b>Flúor:</b> el flúor es necesario para remineralización de los dientes. De ahí la necesidad de utilizar dentífricos con este mineral en la higiene dental.</li>
                <li><b>Falta de información:</b> la falta de información sobre la salud dental lleva a no educar conveniente a los niños desde el primer momento sobre la importancia de la higiene oral y cómo realizarla para evitar complicaciones como la caries o la enfermedad periodontal, entre otras. </li>
            </ul>
